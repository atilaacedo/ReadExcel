package com.atilaacedo.pdffacilitador.model;

public class Sala {
	
	private Integer cod_sala;
	private Integer cod_local;
	private String des_sala;
	public Integer getCod_sala() {
		return cod_sala;
	}
	public void setCod_sala(Integer cod_sala) {
		this.cod_sala = cod_sala;
	}
	public Integer getCod_local() {
		return cod_local;
	}
	public void setCod_local(Integer cod_local) {
		this.cod_local = cod_local;
	}
	public String getDes_sala() {
		return des_sala;
	}
	public void setDes_sala(String des_sala) {
		this.des_sala = des_sala;
	}
	
	
	
	
}
